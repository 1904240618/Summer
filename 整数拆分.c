/*343. 整数拆分
给定一个正整数 n，将其拆分为至少两个正整数的和，并使这些整数的乘积最大化。 返回你可以获得的最大乘积。

示例 1:

输入: 2
输出: 1
解释: 2 = 1 + 1, 1 × 1 = 1。
示例 2:

输入: 10
输出: 36
解释: 10 = 3 + 3 + 4, 3 × 3 × 4 = 36。*/
#include<stdio.h>
#include<string.h>
int integerBreak(int n) 
{
    int dp[n + 1];
    memset(dp, 0, sizeof(dp));
    for (int i = 2; i <= n; i++) 
	{
        int res = 0;
        for (int j = 1; j < i; j++) 
		{
            res = fmax(res, fmax(j * (i - j), j * dp[i - j]));
        }
        dp[i] = res;
    }
    return dp[n];
}
int main()
{
	int n;
	scanf("%d",&n);
	int res = integerBreak(n);
	printf("%d",res);

	return 0;
}